            <h2><span class="mw-headline" id="Overview">Tổng quan</span></h2>
            <p>Giống như hầu hết các game MMORPG khác, có các môn phái khác nhau trong game, tuân
                theo một số nguyên mẫu chung.
                Tuy nhiên, tên của của các mốn phái rất độc đáo vì chúng dựa trên tiểu thuyết Tru
                Tiên nổi tiếng.
                Có 3 chủng tộc trong Tru Tiên bao gồm: Nhân tộc, Thần duệ và Thiên mạch. Các chủng
                tộc đều có sáu môn phái.
                Sau khi thăng thiên, người chơi có tùy chọn thay đổi môn phái của mình. Có vật phẩm
                hỗ trợ đổi từ môn phái này sang môn phái khác.
            </p>

            <h2><span class="mw-headline" id="Ascension">Thăng Thiên</span></h2>
            <p>Thăng thiên là một trong những tính năng quan trọng nhất của game. Nhân vật của bạn sẽ trở nên mạnh mẽ hơn rất nhiều.</p>
            <h2><span class="mw-headline" id="Ascension_Stats">Chỉ số thăng thiên</span></h2>
            <p>Chuyển sinh từ phái này sang phái khác sẽ có những thuộc tính khác nhau. Chi tiết về chỉ số bạn có thể xem <a
                    rel="nofollow" class="external text" href="/chi-so-thang-thien" style="color: red" target="_blank">tại đây</a>
                Đối với Nhân tộc và Thần Duệ, mang lại những hiệu ứng khác nhau của những kỹ năng thăng thiên.
            </p>

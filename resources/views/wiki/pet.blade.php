            <h2><span class="mw-headline" id="Pet_Training">Pet Training</span></h2>
            <p>Tu luyện là cách để tăng các thuộc tính cơ bản của thú nuôi bằng cách sử dụng một số
                vật phẩm nhất
                định như.
                Chỉ cần nói chuyện với Đại Sư Thú Nuôi và chọn dòng "Pet Training".
                Thao tác này sẽ mở ra một cửa sổ hiển thị tất cả các chỉ số của thú nuôi và cung cấp
                cho bạn các tùy
                chọn để sửa đổi hoặc nâng cấp.
            </p>
            <p>Cửa sổ Pet Training có một số nút cho phép bạn nâng cấp một số thuộc tính của thú
                cưng.
                Trong hầu hết các trường hợp, các nút này cũng sẽ cung cấp cho bạn cơ hội mua các
                vật phẩm cần thiết
                để nâng cấp thú nuôi của mình.
            </p>
            <h3><span class="mw-headline" id="Rename">Rename</span></h3>
            <p>Tại cửa sổ Pet Training Nút đổi tên nằm ở góc trên bên trái, bên cạnh tên thú cưng
                của bạn. Với 10
                vàng, bạn có thể đổi tên thú cưng của mình.
            </p>

            <p>You can use Pet Skill Jades to randomly replace and upgrade skills. Advanced Pet
                Skill Jades can be
                purchased in this window.
            </p>
            <ul>
                <li>Cost: History of Domestication (available in the Pet Master's Item Exchange)

                afterwards. You can purchase Snowland Ginseng in this window.
            </p>
            <ul>
                <li>Cost: 588 Sunny Jade, 5 Welkin Red Jade</li>
            </ul>
            <p>When a Pet Hyper Evolves, they will gain an Extra Skill slot.
            </p>

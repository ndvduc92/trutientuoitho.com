            <h3><span class="mw-headline" id="Type_and_Pet_Soul">Type and Pet Soul</span></h3>
            <p>Pet Souls are purchased from the Marketplace, and will change your pet's appearance
                to look like a
                different pet. Additionally, once your pet has reached the appropriate grades, you
                can use Blue
                Mushrooms, Colorful Mushrooms, and Gem Mushrooms to change your pet's appearance.
                You can also use Red
                Mushrooms to revert the effects of the other mushrooms.
            </p>
            <p>You can buy these Mushrooms from the Pet Master's Pet Shop and Item Exchange.
            </p>
            <p>Depending on the pet's type, your pet will have an additional passive bonus.
            </p>
            <ul>
                <li>Avian: Eagle Eye: Increases Pet's basic Accuracy by 15.</li>
                <li>Special: Innate Fortune: Increases Pet's basic Evasion by 15.</li>
                <li>Celestial: Beyond Nature: Increases Pet's basic attributes by 3.</li>
                <li>Beast: Brute Force: Increases pet's basic critstrike damage by 10%.</li>
                <li>Mutant: Rampart Concentration: Damage received by pet decreased by 10.</li>
                <li>Humanoid: Language exchange: Slightly raises basic success rate while training
                    pet.</li>
                <li>Ghost: Will of the Doomed: Increases pet's basic Stun Resistance by 15.
                    Alternatively named:
                    Phantom.</li>
                <li>Fairy: Monstrous: Increases Pet's basic Silence Resistance by 15.</li>
                <li>Delusion: Empty Conclusion: Pet spirit regeneration rate is doubled while out of
                    combat and
                    increased by 50% while in combat.</li>
                <li>Element: Despicable Faith: Increases pet's basic Weaken resistance by 10 and
                    initial defense by
                    10. Alternatively named: Elemental.</li>
                <li>Insect: Futile Effort: Increases damage to normal targets by 20. Alternatively
                    named: Scarab.</li>
                <li>Nature: Radiant Strength: Doubles pet's HP repleneshing speed when not in combat
                    and increases
                    pet's HP repleneshing ]speed while in combat by 50.</li>
                <li>Yasho: Demonic Intuition: Raises pet's basic sleep resistance by
                    15.Alternatively named: Yaoh.
                </li>
            </ul>

            <h3><span class="mw-headline" id="Myna">Myna</span></h3>
            <p>Myna are Affinity items that temporary bonuses to your pet. You can either improve
                your pet's Attack,
                or Defense. The amount of the bonus depends on both your Affinity level, and the
                Grade of your pet. If
                you have Rough Myna Orbs, you can exchange them in this window for Myna you can use
                with your pet. It
                will cost a different amount of Red Mushrooms depending on the quality of Rough Myna
                Orbs that you are
                using.
            </p>
            <h3><span class="mw-headline" id="Trait">Trait</span></h3>
            <p>Your pet's Trait attribute has greater Growth, meaning this particular stat can be
                trained above the
                star sign's respective max growth by approximately 1300 points.
            </p>
            <ul>
                <li>For example, if you train your Airborik pet to the max growth of 7130 Weaken
                    Resistance, and then
                    train it with a Weaken Res trait, the stat can go up to 8340.</li>
            </ul>
            <p>Additionally, whichever Trait your pet has is the type of pet jade you'll receive for
                Releasing the
                pet. Using a Body Strengthen Potion, sold in this window, will randomly change your
                pet's Trait. Your
                pet's current Growths will not be changed.
            </p>
            <ul>
                <li>Cost: 50 Sunny Jade</li>
            </ul>
            <h3><span class="mw-headline" id="Pet_Age">Pet Age</span></h3>
            <p>Your pet's Age/Life determines how long it will live. If your pet's Age reaches the
                maximum value,
                the pet will die and cannot be summoned again until its Age is reduced, or its
                maximum value is
                increased. You can buy Age reducing items and Life increasing items in the Pet
                Master's Pet Shop.
            </p>
            <h3><span class="mw-headline" id="Pet_Growth">Pet Growth</span></h3>
            <p>Pet Growth determines your pet's stats. The higher the Growth, the better your pet's
                stats will be.
                Additionally, the higher your pet's Growth, the higher Grade your pet will be. At
                high Grades, you
                will be able to evolve your pet's appearance.
            </p>
            <p>To upgrade your pet's Growth, you will need either Pet Jade or Celebeans.
            </p>
            <p>In the Pet Growth window, you have three different tabs that determine the growth
                points and celebean
                cost.
                Next, click on the type and amount of jade you would like to use.
            </p>
            <p>Once you have selected the number of attempts, click on the Confirm button to begin
                training your
                pet. Be aware that training can fail. When training succeeds, the growth will
                increase by the full
                amount, but when it fails, the growth will only increase by a few points, if it
                increases at all. The
                higher the growth, the higher the chances that it will fail.
            </p>
            <h3><span class="mw-headline" id="Pet_Jade">Pet Jade</span></h3>
            <p>Pet Jades can be earned by Releasing pets or directly using Celebeans in the "Pet
                Growth" window.
            </p>
            <p>When releasing pets, such as the ones from the Free Lunch quest, you are given Pet
                Jade in return.
                The number and quality of jades given depend on the Grade of the pet you are
                releasing. The pet's
                Trait determines what kind of Pet Jade you receive.
            </p>
            <p>Different types of pet jade raise different stats:
            </p>
            <ul>
                <li>Bloodspirit = Health</li>
                <li>Glory = Spirit</li>
                <li>Dragon = Min Attack</li>
                <li>Imperial = Max Attack</li>
                <li>Tortoise = Defense</li>
                <li>Heaveneye = Accuracy</li>
                <li>Windwhirl = Evasion</li>
                <li>Tiger = Stun Resistance</li>
                <li>Bear = Weaken Resistance</li>
                <li>Flyshadow = Silence Resistance</li>
                <li>Windsong = Sleep Resistance</li>
                <li>Serpent = Paralyze Resistance</li>
            </ul>
            <p>There are also several ranks of pet jades:
            </p>
            <ul>
                <li>Piece: Increases by up to 10. Cannot be used on Grade 8 or above pets.</li>
                <li>Minor: Increases by up to 20. Cannot be used on Grade 12 or above pets.</li>
                <li>Normal: Increases by up to 40 points. Cannot be used on Grade 18 or above pets.
                </li>
                <li>Improved: Increases by up to 100 points. Can be used on pets of any Grade.</li>
            </ul>

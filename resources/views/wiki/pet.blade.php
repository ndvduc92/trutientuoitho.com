            <h2><span class="mw-headline" id="Pet_Skills">Pet Skills</span></h2>
            <p>When your pet is summoned, a new skill bar appears near your own. Most of these
                skills deal
                additional damage to the target, and many have additional effects, from decreasing
                the target's Attack
                Power to removing Bleed effects from the pet's master.
            </p>
            <p>You can either left-click on these skills to use them manually, or right-click on
                them to set them to
                auto-cast when their cooldown is over. If your pet runs out of Spirit, it will be
                unable to use
                skills, so make sure you have plenty of points left on your pet's remedy battery.
            </p>
            <h3><span class="mw-headline" id="Learning_and_Upgrading_Skills">Learning and
                    Upgrading Skills</span>
            </h3>
            <p>When a pet is first tamed, it will know a random number of skills. Its first skill is
                determined
                entirely by its Star Sign. A pet can have up to 5 additional skills randomly
                assigned from common or
                Sign skills.
            </p>
            <p>If you would like to change what skills your pet knows, you can train it with an
                unlearned Pet Skill
                Jade. These items can be found inside random mystery packs sold by the Pet Tamer, or
                Sacred Spirit
                Scrolls from Bosses/Instances. Training your pet with one of these items will
                replace one of its
                existing skills at random.
            </p>
            <p>If you train your pet with a skill jade for a skill that it already knows, that skill
                will increase
                in rank and become more powerful. Pet Tamer Totto's skill jades can only increase a
                skill's rank up to
                31. Past that point, you will need to trade the rare History of Domestication to Pet
                Tamer Lonno in
                Sunstream in exchange for higher grade skill jades that can increase a skill up to
                rank 61. Or open
                higher grade Sacred Spirit Scrolls.
            </p>
            <p>In addition to using skill jades, your pet's skills are also increased in rank by its
                Specialty.
                Every skill is enhanced by one specialty. A main specialty (marked with a sun on
                your pet's Stats
                sheet) increases the rank of all associated skills by 30, while a sub-specialty
                (marked with a moon)
                increases the rank of all associated skills by 10. This is on top of the ranks
                gained by skill jades,
                so a skill that is your pet's main specialty can be increased to a maximum of 91,
                and a skill that is
                your pet's sub-specialty can be increased to a maximum of 71.
            </p>
            <p>Signature Skills cost 25 Spirit at Rank 1, 50 Spirit at Rank 2, and 75 Spirit at Rank
                3. All other
                pet skills cost 15 Spirit at Rank 1, and increase in cost by 5 Spirit per Rank.
            </p>
            <h3><span class="mw-headline" id="Expand_Skill">Expand Skill</span></h3>

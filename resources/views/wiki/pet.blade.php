            <h2><span class="mw-headline" id="Pet_Gear">Pet Gear</span></h2>
            <h3><span class="mw-headline" id="Pet_Workshop">Pet Workshop</span></h3>
            <p>The Workshop uses items gathered by pets while foraging to craft Pet Gear and
                remedies, including
                Mirage Candy that grants EXP to your pets. The Pet Workshop is the only way to craft
                the very best
                gear for your pets as High-level recipes require high Tech Stats and Development.
            </p>
            <p>You must have a high enough crafting level in order to learn Pet Workshop recipes.
                Basic Pet Gear
                recipes require a Craft Level of 1, while +1 and +2 Pet Gear recipes require a Craft
                Level of 2.
            </p>
            <p>All level 60+ pets will automatically add their Aptitudes to the Workshop's Tech
                Stats (Level 60+
                pets are marked on the Pet Roster with a (*) in front of their name). Tech Stats can
                be further
                increased by setting pets to Crafting Classes. Having multiple types of high-level
                pets is very
                important for crafting high-level items.
                Workshop Development increases as you craft items in the Pet Workshop.
                How to get Workshop Recipes
                Click on the slot names below to see the recipes available for that slot.
            </p>
            <p>Most pet gear recipes are obtained through Foraging:
            </p>
            <ul>
                <li>Farming: Claw + Relic</li>
                <li>Woodwork: Armor + Vest</li>
                <li>Hunting: Aura + Dream</li>
                <li>Fishing: Gem + Legacy</li>
                <li>Mining: Mask + Pendant</li>
                <li>Treasure: Sutra + Mystery</li>
            </ul>
            <p>Recipes for various Health and Spirit pet consumables as well as a few low-level pet
                gear items can
                be purchased from Pet Tamer Totto.
            </p>
            <p>Recipes for Mirage Candy pet EXP consumables and a few basic pieces of gear are
                obtained from quests
                given by Pet Master Ellan once you have reached the required levels:
            </p>
            <ul>
                <li>Level 30: Mirage Candy I (Increases your pet's experience by 20000 points.)</li>
                <li>Level 62+: recipes for basic mask, claw, armor, aura, sutra, and pendant gear.
                </li>
                <li>Level 70+: Mirage Candy II (Increases your pet's experience by 30000 points.)
                </li>
                <li>Level 100+: Mirage Candy III (Increases your pet's experience by 40000 points.)
                </li>
                <li>Level 115+: Mirage Candy IV (Increases your pet's experience by 60000 points.)
                </li>
                <li>Level 130+: Mirage Candy V (Increases your pet's experience by 90000 points.)
                </li>
                <li>Level 140+: Mirage Candy VI (Increases your pet's experience by 130000 points.)
                </li>
            </ul>
            <h3><span class="mw-headline" id="Fortify_Pet_Gear">Fortify Pet Gear</span></h3>
            <p>All crafted Pet Gear has three ranks.
            </p>
            <ul>
                <li>Basic: Requires Grade 4</li>
                <li>+1: Requires Grade 8</li>
                <li>+2: Requires Grade 12</li>
            </ul>
            <p>While it is possible to craft +1 and +2 gear in the Pet Workshop, the main way of
                upgrading your gear
                is by taking it to a Pet Master and selecting the "Fortify Gear" option.
            </p>
            <p>Two items are used with this option:
            </p>
            <ul>
                <li>Jadefog Stone: Upgrades Basic Pet Gear to +1 Pet Gear.</li>
                <li>Bluemoon Stone: Upgrades +1 Pet Gear to +2 Pet Gear.</li>
            </ul>
            <p>These items can be randomly found by Pets when they are Foraging for materials.
            </p>
            <p>Attempting to upgrade your gear in this way will not destroy the gear if it fails.
            </p>



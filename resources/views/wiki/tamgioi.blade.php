            <h2><span class="mw-headline" id="Overview">Tổng quan</span></h2>
            <p>Trang này bao gồm thông tin chung có liên quan về tam giới. Vui lòng xem bài viết chính của từng giới để
                biết thông tin cụ thể hơn.
            </p>
            <p>Sau khi thăng thiên và đạt đến cấp 90, người chơi có thể hoàn thành nhiệm vụ để gia nhập một trong ba
                giới sau: Tiên, Phật hoặc Ma.
                Mỗi hệ có hệ thống kỹ năng riêng, 8 kỹ năng chung và 5 kỹ năng dành riêng cho phái.
                Mỗi hệ cũng có quyền truy cập độc quyền vào một trong ba bản đồ giới sau: Tiên giới, Phật giới và Ma
                giới.
            </p>
            <h2><span class="mw-headline" id="Affinities">Tam giới</span></h2>
            <h3><span class="mw-headline" id="Felkin">Tiên</span></h3>
            <p>The Felkin Affinity is typically known for its offensive style. Felkin skills typically deal with attack
                power, personal buffs, and enemy debuffs.
            </p>
            <h3><span class="mw-headline" id="Fuwa">Phật</span></h3>
            <p>The Fuwa Affinity is typically known for its defensive style. Fuwa skills typically deal with the health,
                defense, and resistance stats of the caster, party, or enemy. This affinity also has strong ties with
                pets.
            </p>
            <h3><span class="mw-headline" id="Dagos">Ma</span></h3>
            <p>The Dagos Affinity is typically known for its mobile offensive style and comparative defensive drawback.
                Many players refer to this as a "glass cannon" playstyle. Dagos skills typically deal with spirit,
                resistances, and mobility.
            </p>
